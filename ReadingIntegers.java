import java.util.*;
public class ReadIntegers {
	public static void main(String []args) {
	Scanner input = new Scanner(System.in);
	int sum = 0;
	int totalCounter = 1;
	while (totalCounter <=  100) {
	System.out.print("Enter next integer ");
	int nextNumber = input.nextInt();
	}
	total = sum + nextNumber;
	totalCounter = totalCounter + 10;
	System.out.printf("Total sum of values is greater than 100 ", total);
       
   }
 }